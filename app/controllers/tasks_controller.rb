class TasksController < ApplicationController
  # GET /tasks
  # GET /tasks.xml
  def index
    @task = Task.new
    @no_due_date_tasks = Task.find(:all, :conditions => ["complete IS NULL AND due IS NULL"])
    @overdue_tasks = Task.find(:all, :conditions => ["complete IS NULL AND due < ?", Time.now], :order => "due")
    @incomplete_tasks = Task.find(:all, :conditions => ["complete IS NULL AND due >= ?", Time.now], :order => "due")
    @completed_tasks = Task.find(:all, :conditions => ["complete IS NOT NULL"], :order => "complete")
    @pending_tasks =  [@overdue_tasks,@incomplete_tasks,@no_due_date_tasks]
    respond_to do |format|
      format.html # index.html.erb
      format.xml  { render :xml => @tasks }
    end
  end

  # GET /tasks/1
  # GET /tasks/1.xml
  def show
    @task = Task.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.xml  { render :xml => @task }
    end
  end

  # GET /tasks/new
  # GET /tasks/new.xml
  def new
    @task = Task.new

    respond_to do |format|
      format.html # new.html.erb
      format.xml  { render :xml => @task }
    end
  end

  # GET /tasks/1/edit
  def edit
    @task = Task.find(params[:id])
  end

  # POST /tasks
  # POST /tasks.xml
  def create
    @task = Task.new(params[:task])

    respond_to do |format|
      if @task.save
        format.html { redirect_to(:root, :notice => 'Task was successfully created.') }
        format.xml  { render :xml => :roo, :status => :created, :location => @task }
      else
        format.html { render :action => "new" }
        format.xml  { render :xml => @task.errors, :status => :unprocessable_entity }
      end
    end
  end

  # PUT /tasks/1
  # PUT /tasks/1.xml
  def update
    @task = Task.find(params[:id])

    respond_to do |format|
      if @task.update_attributes(params[:task])
        format.html { redirect_to(:root, :notice => 'Task was successfully updated.') }
        format.xml  { head :ok }
      else
        format.html { render :action => "edit" }
        format.xml  { render :xml => @task.errors, :status => :unprocessable_entity }
      end
    end
  end

  # DELETE /tasks/1
  # DELETE /tasks/1.xml
  def destroy
    @task = Task.find(params[:id])
    @task.destroy

    respond_to do |format|
      format.html { redirect_to(tasks_url) }
      format.xml  { head :ok }
    end
  end
end
